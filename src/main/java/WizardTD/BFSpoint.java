package WizardTD;

public class BFSpoint {
    private int Y;
    private int X;

     public BFSpoint() {

    }
    public BFSpoint(int y, int x) {
        Y = y;
        X = x;
    }
    public int getY() {
        return Y;
    }
    public int getX() {
        return X;
    }

    
    
}
